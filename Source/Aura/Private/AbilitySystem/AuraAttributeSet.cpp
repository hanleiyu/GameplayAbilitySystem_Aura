void UAuraAttributeSet::SetEffectProperty(const FGameplayEffectModCallbackData& Data, FEffectProperty& Prop) const
{
	Prop.EffectContextHandle = Data.EffectSpec.GetEffectContext();

	Prop.SourceASC = Data.EffectSpec.GetEffectContext().GetOriginalInstigatorAbilitySystemComponent();

	if (IsValid(Prop.SourceASC) && Prop.SourceASC->AbilityActorInfo.IsValid() && Prop.SourceASC->AbilityActorInfo->AvatarActor.IsValid())
	{
		Prop.SourceAvatarActor = Prop.SourceASC->AbilityActorInfo->AvatarActor.Get();
		Prop.SourceController = Prop.SourceASC->AbilityActorInfo->PlayerController.Get();
		if (Prop.SourceController == nullptr && Prop.SourceAvatarActor != nullptr)
		{
			if (Cast<APawn>(Prop.SourceAvatarActor))
			{
				Prop.SourceController = Cast<APawn>(Prop.SourceAvatarActor)->GetController();
			}
		}
		if (Prop.SourceController != nullptr)
		{
			Prop.SourceCharacter = Prop.SourceController->GetCharacter();
		}
	}
	

	Prop.TargetASC = &Data.Target;

	if (Prop.TargetASC->AbilityActorInfo.IsValid() && Prop.TargetASC->AbilityActorInfo->AvatarActor.IsValid())
	{
		Prop.TargetAvatarActor = Prop.TargetASC->AbilityActorInfo->AvatarActor.Get();
		Prop.TargetController = Prop.TargetASC->AbilityActorInfo->PlayerController.Get();
		if (Prop.TargetController == nullptr && Prop.TargetAvatarActor != nullptr)
		{
			if (Cast<APawn>(Prop.TargetAvatarActor))
			{
				Prop.TargetController = Cast<APawn>(Prop.TargetAvatarActor)->GetController();
			}
		}
		if (Prop.TargetController != nullptr)
		{
			Prop.TargetCharacter = Prop.TargetController->GetCharacter();
		}
	}
	
}